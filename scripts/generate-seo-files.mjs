/**
 * Generates public/sitemap.xml, llms.txt, and llms-full.txt from site data.
 * Run before build: npm run seo (or via the build script).
 */
import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { POSTS } from "../src/data/posts.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const publicDir = join(root, "public");

const { destinations: DESTINATIONS } = JSON.parse(
  readFileSync(join(root, "src/data/destinations/index.json"), "utf8")
);

const SITE_URL = "https://travel.gyrogovernance.com";

const DOMAINS = [
  { slug: "economy", name: "Economy", tagline: "Keep your spending in the places you visit." },
  { slug: "employment", name: "Employment", tagline: "Respect the people whose work makes travel possible." },
  { slug: "education", name: "Education", tagline: "Learn before you go, and learn while you are there." },
  { slug: "ecology", name: "Ecology", tagline: "Leave the land and sea better than you found them." },
];

const STATIC_PAGES = [
  { path: "/", priority: "1.0", changefreq: "weekly", title: "Home" },
  { path: "/guides", priority: "0.9", changefreq: "weekly", title: "Guides hub" },
  { path: "/destinations", priority: "0.9", changefreq: "weekly", title: "Destinations" },
  { path: "/resources", priority: "0.8", changefreq: "monthly", title: "Travel Resources" },
  { path: "/search/flights", priority: "0.85", changefreq: "weekly", title: "Search Flights" },
  { path: "/prompts", priority: "0.8", changefreq: "monthly", title: "AI Prompts" },
  { path: "/about", priority: "0.7", changefreq: "monthly", title: "About" },
  { path: "/privacy", priority: "0.3", changefreq: "yearly", title: "Privacy" },
  { path: "/cookies", priority: "0.3", changefreq: "yearly", title: "Cookies" },
];

function pageUrl(path) {
  if (!path || path === "/") return `${SITE_URL}/`;
  return `${SITE_URL}${path.startsWith("/") ? path : `/${path}`}`;
}

function escapeXml(s) {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function buildSitemap() {
  const urls = [];

  for (const page of STATIC_PAGES) {
    urls.push({ loc: pageUrl(page.path), priority: page.priority, changefreq: page.changefreq });
  }

  for (const d of DOMAINS) {
    urls.push({
      loc: pageUrl(`/domains/${d.slug}`),
      priority: "0.8",
      changefreq: "monthly",
    });
  }

  for (const p of POSTS) {
    urls.push({
      loc: pageUrl(`/guides/${p.slug}`),
      priority: "0.7",
      changefreq: "monthly",
      lastmod: p.date,
    });
  }

  for (const d of DESTINATIONS) {
    urls.push({
      loc: pageUrl(`/destinations/${d.slug}`),
      priority: "0.7",
      changefreq: "monthly",
    });
  }

  const body = urls
    .map((u) => {
      let block = `  <url>\n    <loc>${escapeXml(u.loc)}</loc>`;
      if (u.lastmod) block += `\n    <lastmod>${u.lastmod}</lastmod>`;
      if (u.changefreq) block += `\n    <changefreq>${u.changefreq}</changefreq>`;
      if (u.priority) block += `\n    <priority>${u.priority}</priority>`;
      block += "\n  </url>";
      return block;
    })
    .join("\n");

  return `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-seo-files.mjs -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${body}
</urlset>
`;
}

function buildLlmsTxt({ full }) {
  const lines = [
    "# Gyro Governance Ethical Travel",
    "",
    "> Ethical travel guidance across Economy, Employment, Education, and Ecology.",
    "> AI-empowered guides for human adventures from Gyro Governance.",
    "",
    `Site: ${SITE_URL}`,
    `Canonical pattern: ${SITE_URL}/<path>`,
    "",
  ];

  if (full) {
    lines.push("## About", "", SITE_DESCRIPTION(), "", "## Main pages", "");
  } else {
    lines.push("## Main pages", "");
  }

  for (const page of STATIC_PAGES) {
    if (page.path === "/privacy" || page.path === "/cookies") continue;
    lines.push(`- [${page.title}](${pageUrl(page.path)}): ${page.title}`);
  }

  lines.push("", "## Four domains", "");
  for (const d of DOMAINS) {
    lines.push(`- [${d.name}](${pageUrl(`/domains/${d.slug}`)}): ${d.tagline}`);
  }

  lines.push("", "## Destinations", "");
  if (full) {
    lines.push(`Hub: ${pageUrl("/destinations")}`, "");
    for (const d of DESTINATIONS.slice(0, 20)) {
      lines.push(`### ${d.name}, ${d.country}`, "");
      lines.push(`- URL: ${pageUrl(`/destinations/${d.slug}`)}`);
      lines.push(`- Region: ${d.region}`);
      lines.push(`- Summary: ${d.teaser}`, "");
    }
    if (DESTINATIONS.length > 20) {
      lines.push(`... and ${DESTINATIONS.length - 20} more on the destinations hub.`, "");
    }
  } else {
    lines.push(
      `- [Destinations](${pageUrl("/destinations")}): ${DESTINATIONS.length} mainstream destinations with ethical planning prompts`
    );
  }

  lines.push("", "## Travel guides (articles)", "");
  for (const p of POSTS) {
    const domain = DOMAINS.find((d) => d.slug === p.domain);
    const label = domain ? `${domain.name} - ` : "";
    if (full) {
      lines.push(`### ${p.title}`, "");
      lines.push(`- URL: ${pageUrl(`/guides/${p.slug}`)}`);
      lines.push(`- Domain: ${domain?.name ?? p.domain}`);
      lines.push(`- Published: ${p.date}`);
      lines.push(`- Summary: ${p.excerpt}`, "");
    } else {
      lines.push(`- [${p.title}](${pageUrl(`/guides/${p.slug}`)}): ${label}${p.excerpt}`);
    }
  }

  if (!full) {
    lines.push("", "## More", "");
    lines.push(`- [Privacy](${pageUrl("/privacy")})`);
    lines.push(`- [Cookies](${pageUrl("/cookies")})`);
    lines.push(`- [Full index](${SITE_URL}/llms-full.txt): all guides with summaries`);
    lines.push(`- [XML sitemap](${SITE_URL}/sitemap.xml)`);
  }

  lines.push("");
  return lines.join("\n");
}

function SITE_DESCRIPTION() {
  return (
    "Gyro Governance Ethical Travel helps visitors use AI to plan moral trips, " +
    "support local communities, and explore through four domains: Economy, Employment, " +
    "Education, and Ecology."
  );
}

writeFileSync(join(publicDir, "sitemap.xml"), buildSitemap(), "utf8");
writeFileSync(join(publicDir, "llms.txt"), buildLlmsTxt({ full: false }), "utf8");
writeFileSync(join(publicDir, "llms-full.txt"), buildLlmsTxt({ full: true }), "utf8");

console.log("Wrote public/sitemap.xml, llms.txt, llms-full.txt");
